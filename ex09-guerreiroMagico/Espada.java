public class Espada {
    
    private int forcaEspada = 10;
    private String nomeEspada;
    
    public int getForca(){
        return forcaEspada;
    }
    
    public void setForca(int forca){
        this.forcaEspada = forca;
    }
    
    public String getNome(){
        return nomeEspada;
    }
    
    public void setNome(String nome){
        this.nomeEspada = nome;
    }
    
}