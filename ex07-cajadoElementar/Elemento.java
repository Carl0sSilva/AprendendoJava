public class Elemento{
    
    private String elemento;
    private int forca;
    
    public String getElemento(){
        return elemento;
    }
    
    public void setElemento(String elemento){
        this.elemento = elemento;
    }
    
    public int getForca(){
        return forca;
    }
    
    public void setForca(int forca){
        this.forca = forca;
    }
    
}